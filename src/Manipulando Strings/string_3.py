nome = "Guilherme Gomes dos Santos"

print(nome[0]) # retorna a primeira posição do indice da variavel nome

print(nome[:19])# retorna des do indice 0 da variavel até o indice 19

print(nome[10:])# retorna des do indice 10 da variavel até o final do indice

print(nome[10:16])# retorna a partir do indice 10 da variavel até o indice 16

print(nome[10:16:2])# retorna a partir do indice 10 da variavel até o indice 16 porem vai pulando de 2 em 2

print(nome[:]) # irá imprimir a variavel completa

print(nome[:: -1])# retorna o inverso da variavel